package udpSend.Receive;
//coordiantes class with basic getter and setter

public class Coordinates {
	public float x;
	public float y;
	
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
}
